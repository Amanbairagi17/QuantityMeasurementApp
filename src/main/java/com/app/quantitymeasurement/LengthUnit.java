package com.app.quantitymeasurement;

public enum LengthUnit {

    FEET(1.0),
    INCH(1.0 / 12.0),
    YARDS(3.0),                    
    CENTIMETERS(0.393701 / 12.0);

    private final double toBase;

    LengthUnit(double toBase) {
        this.toBase = toBase;
    }

    public double toFeet(double value) {
        return value * toBase;
    }

}
